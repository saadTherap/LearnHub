SELECT
    tl_final.id,
    tl_final.name,
    tl_final.release -- This is the specific content version for the contentRelease ID
FROM (
         SELECT
             tl_inner.*,
             -- Assign a row number to each contentRelease version for a given logical contentRelease ID.
             -- We order by 'release' DESC to ensure the most recent content version for that ID comes first.
             ROW_NUMBER() OVER (PARTITION BY tl_inner.id ORDER BY tl_inner.release DESC) AS rn
         FROM
             TEST_LESSON tl_inner
         WHERE
           -- Condition 1: The contentRelease content version must have been released at or before the target course release.
             tl_inner.release <= :specific_release_number

           -- Condition 2: The contentRelease content version must NOT have been marked as ended/deleted
           -- by the time of the target course release.
           -- A contentRelease is active for :specific_release_number if:
           --   a) Its 'release_end' is NULL (meaning it's currently active/never ended)
           --   OR
           --   b) Its 'release_end' is 0 (explicitly treating 0 as active indefinitely, based on your data)
           --   OR
           --   c) Its 'release_end' is GREATER THAN the :specific_release_number
           --      (meaning it was still active AT the :specific_release_number,
           --       and only ended/deleted in a later course release).
           AND (tl_inner.release_end IS NULL OR tl_inner.release_end = 0 OR tl_inner.release_end > :specific_release_number)

           -- Condition 3: The specific contentRelease content version itself is not marked as deleted.
           -- This handles cases where a contentRelease version might be marked as deleted,
           -- but its 'release_end' might be for a future course release.
           AND tl_inner.is_deleted = 0
     ) tl_final
WHERE
    -- Select only the latest content version (rn = 1) for each logical contentRelease ID
    -- that satisfied all the above conditions for the specified course release number.
    tl_final.rn = 1;

