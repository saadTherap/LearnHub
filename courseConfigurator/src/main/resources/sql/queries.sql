CREATE TABLE TEST_COURSE (
                             id                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             name              VARCHAR2(255) NOT NULL,
                             current_release   NUMBER(10) DEFAULT 1 NOT NULL
);

CREATE TABLE TEST_MODULE (
                             id                NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             name              VARCHAR2(255) NOT NULL,
                             course_id         NUMBER(19) NOT NULL,
                             CONSTRAINT fk_test_module_course
                                 FOREIGN KEY (course_id)
                                     REFERENCES TEST_COURSE (id)
                                         ON DELETE CASCADE -- If a course is deleted, its modules are also deleted
);

CREATE TABLE TEST_LESSON (
                             id                NUMBER(19) NOT NULL, -- Logical ID of the contentRelease (e.g., L1)
                             release           NUMBER(10) NOT NULL, -- Version number of this specific contentRelease content
                             module_id         NUMBER(19) NOT NULL,
                             name              VARCHAR2(255) NOT NULL,
                             is_deleted        NUMBER(1) DEFAULT 0 NOT NULL, -- 0 for false, 1 for true
                             release_end       NUMBER(10),                   -- Nullable: indicates the end version this content was active, or null if current
                             CONSTRAINT pk_test_lesson PRIMARY KEY (id, release), -- Composite Primary Key
                             CONSTRAINT fk_test_lesson_module
                                 FOREIGN KEY (module_id)
                                     REFERENCES TEST_MODULE (id)
                                         ON DELETE CASCADE -- If a module is deleted, its contentReleases are also deleted
);

