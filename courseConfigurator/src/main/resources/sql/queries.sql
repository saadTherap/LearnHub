-- Working query for finding course lectures of a SPECIFIC VERSION of COURSE
SELECT
    tl_final.id,
    tl_final.name,
    tl_final.release AS lesson_content_version,
    tl_final.is_deleted,
    tl_final.release_end
FROM (
         SELECT
             tl_inner.*,
             ROW_NUMBER() OVER (PARTITION BY tl_inner.id ORDER BY tl_inner.release DESC) AS rn
         FROM
             TEST_LESSON tl_inner
         WHERE
             tl_inner.release <= :specific_release_number
--            AND (tl_inner.release_end IS NULL OR tl_inner.release_end = 0 OR tl_inner.release_end >= :specific_release_number)
--        AND tl_inner.is_deleted = 0
     ) tl_final
WHERE
    tl_final.rn = 1
AND (
    :specific_release_number < release_end OR release_end = 0
    );
